    public UpdateSylabus(String revisions,String sem) {
        initComponents();
        this.setLocationRelativeTo(null);
        System.out.println("revision code " + code);
        System.out.println("semester" + sem);
        Dbcon db = new Dbcon();
        ResultSet rs = db.select("select*from tbl_syllubus where revision_code='"+ code +"' AND semetser = '"+sem+"'");
        try {
            if (rs.next()) {
                code.setText(rs.getString("revision_code"));
                branch.setSelectedItem(rs.getString("branch"));
                semester.setSelectedItem(rs.getString("semester"));
                subject.setSelectedItem(rs.getString("subject_name"));
                subc.setText(rs.getString("subject_code"));
                mod1.setText(rs.getString("module_1"));
                mod2.setText(rs.getString("module_2"));
                mod3.setText(rs.getString("module_3"));
                mod4.setText(rs.getString("module_4"));
               

            }
        } catch (SQLException ex) {
            Logger.getLogger(UpdateSylabus.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
       
